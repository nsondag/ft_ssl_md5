#include "libft.h"

int		count(intmax_t n, int base)
{
	int count;

	count = 0;
	if (n < 0)
	{
		if (n < -9223372036854775807)
			return (19);
		n *= -1;
	}
	if (n == 0)
		return (1);
	while (n > 0)
	{
		n = n / base;
		count++;
	}
	return (count);
}
